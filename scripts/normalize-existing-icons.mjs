#!/usr/bin/env node
/**
 * Re-normalizes all public/icons SVG files and regenerates lib/icons/generated.ts.
 * Usage: node scripts/normalize-existing-icons.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { normalizeSvgMarkup, VIEW_SIZE } from "./lib/icon-geometry.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const iconsDir = path.join(root, "public/icons");
const outFile = path.join(root, "lib/icons/generated.ts");

function toKey(id, mode) {
  return `${id.replace(/\//g, "--")}__${mode}`;
}

function walkSvgFiles(dir, base = iconsDir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkSvgFiles(full, base));
    } else if (entry.name.endsWith(".svg")) {
      const rel = path.relative(base, full).replace(/\\/g, "/");
      files.push({ full, rel });
    }
  }
  return files;
}

function relToIdMode(relPath) {
  const withoutExt = relPath.replace(/\.svg$/i, "");
  const lastSlash = withoutExt.lastIndexOf("/");
  const id = withoutExt.slice(0, lastSlash);
  const mode = withoutExt.slice(lastSlash + 1);
  return { id, mode };
}

const files = walkSvgFiles(iconsDir);
const registry = {};
let normalized = 0;
let skipped = 0;

for (const { full, rel } of files) {
  const { id, mode } = relToIdMode(rel);
  const raw = fs.readFileSync(full, "utf8");
  const inner = normalizeSvgMarkup(raw);
  const fullSvg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${VIEW_SIZE} ${VIEW_SIZE}" fill="none">${inner}</svg>`;
  fs.writeFileSync(full, fullSvg, "utf8");
  registry[toKey(id, mode)] = inner;
  normalized += 1;
}

const lines = [
  "/** Auto-generated by scripts/sync-figma-icons.mjs — do not edit manually. */",
  `/** Normalized: ${normalized} icon variants (scripts/normalize-existing-icons.mjs) */`,
  "export const iconSvgByKey = {",
  ...Object.entries(registry)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([key, inner]) => `  "${key}": ${JSON.stringify(inner)},`),
  "} as const;",
  "",
  "export type GeneratedIconKey = keyof typeof iconSvgByKey;",
];

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, lines.join("\n"), "utf8");

console.log(`Normalized ${normalized} SVG files under public/icons/`);
console.log(`Wrote ${outFile}`);
