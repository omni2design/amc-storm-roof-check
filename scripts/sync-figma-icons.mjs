#!/usr/bin/env node
/**
 * Downloads SVG icons from Figma MCP asset URLs and generates lib/icons/generated.ts.
 * Supports single `url` or layered `urls[]` for composite Figma icons.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { normalizeIconInner, VIEW_SIZE } from "./lib/icon-geometry.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const manifestPath = path.join(__dirname, "figma-icon-manifest.json");
const iconsDir = path.join(root, "public/icons");
const outFile = path.join(root, "lib/icons/generated.ts");

const BRAND_COLORS = [
  "#0b1f33",
  "#0B1F33",
  "#1f3a5f",
  "#1F3A5F",
  "#b22e2e",
  "#B22E2E",
  "#6b7280",
  "#4b5563",
  "#ffffff",
  "#FFFFFF",
];

function normalizeSvg(raw) {
  let svg = raw.trim();
  if (!svg.startsWith("<svg")) return svg;

  svg = svg.replace(/\s(width|height)="[^"]*"/gi, "");
  for (const hex of BRAND_COLORS) {
    const re = new RegExp(hex.replace("#", "#"), "gi");
    svg = svg.replace(re, "currentColor");
  }
  svg = svg.replace(/stroke="(?!currentColor|none)[^"]+"/gi, 'stroke="currentColor"');
  svg = svg.replace(/fill="(?!none|currentColor)[^"]+"/gi, 'fill="currentColor"');

  return svg.replace(/^<svg[^>]*>/, "").replace(/<\/svg>\s*$/, "").trim();
}

function toKey(id, mode) {
  return `${id.replace(/\//g, "--")}__${mode}`;
}

async function fetchLayer(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return normalizeSvg(await res.text());
}

async function main() {
  const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
  fs.mkdirSync(iconsDir, { recursive: true });

  const registry = {};
  let synced = 0;

  for (const entry of manifest.icons) {
    const { id, mode, figmaNode, url, urls } = entry;
    const sources = urls ?? (url ? [url] : []);
    if (sources.length === 0) {
      console.error(`No URL for ${id} (${figmaNode})`);
      process.exitCode = 1;
      continue;
    }

    const relPath = `${id}/${mode}.svg`;
    const filePath = path.join(iconsDir, relPath);
    fs.mkdirSync(path.dirname(filePath), { recursive: true });

    try {
      const layers = [];
      for (const source of sources) {
        layers.push(await fetchLayer(source));
      }
      const rawInner = layers.join("");
      const inner = normalizeIconInner(rawInner);
      const fullSvg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${VIEW_SIZE} ${VIEW_SIZE}" fill="none">${inner}</svg>`;
      fs.writeFileSync(filePath, fullSvg, "utf8");

      registry[toKey(id, mode)] = inner;
      synced += 1;
      console.log(`✓ ${relPath}${sources.length > 1 ? ` (${sources.length} layers)` : ""}`);
    } catch (err) {
      console.error(`✗ ${id} (${figmaNode}): ${err.message}`);
      process.exitCode = 1;
    }
  }

  const lines = [
    "/** Auto-generated by scripts/sync-figma-icons.mjs — do not edit manually. */",
    `/** Synced: ${synced} icon variants */`,
    "export const iconSvgByKey = {",
    ...Object.entries(registry).map(([key, inner]) => `  "${key}": ${JSON.stringify(inner)},`),
    "} as const;",
    "",
    "export type GeneratedIconKey = keyof typeof iconSvgByKey;",
  ];

  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, lines.join("\n"), "utf8");
  console.log(`\nWrote ${outFile} (${synced} variants)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
